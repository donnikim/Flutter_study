void main() {
  // 변수 타입 선언자
  // 정수
  //heap메모리에 적재
  int iNum1 = 30;
  print(iNum1);

  int iNum2 = 50;
  print(iNum2);
  // 사칙연산
  print(iNum1 + iNum2);
  print(iNum1 - iNum2);
  print(iNum1 * iNum2);
  print(iNum1 / iNum2);
  print(iNum1 % iNum2);
  print(iNum1 ~/ iNum2); // 몫을 구하는 구조

  double dNum1 = 1.5;
  double dNum2 = 0.5;

  print(dNum1 + dNum2);
  print(dNum1 - dNum2);
  print(dNum1 * dNum2);
  print(dNum1 / dNum2);
  print(dNum1 % dNum2);
  print(dNum1 ~/ dNum2);
}
